<h1>Frequently Asked Questions</h1>

<ol>
	<li><strong>I am a member (architect ie) but the developer of the project is not a member. Is the project eligible to enter?</strong><br />
		No, the developer of the project must be a UDI member.
	</li>
 
	<li><strong>I am a member (developer) but some of the development team are not members (architect, engineer, ie).  Can they enter the project on my behalf?</strong><br />
            Yes, providing the developer gives consent.
	</li>
	
	<li><strong>Is the project team recognized at the ceremony?</strong><br />
            Yes, although the Award goes to the developer, members of the project team are announced. 
	</li>
	
	<li><strong>I am unsure about the criteria and which category to enter.  How do I get more information? </strong><br />
            Email your question to the Awards information desk <a href="mailto:gnormandeau@udi.bc.ca">gnormandeau@udi.bc.ca</a> or phone the UDI office. 
 	</li>
	<li><strong>When will the judging take place?</strong><br />
		The Jury will review the submissions during the month of August and some projects may be visited during the month of September.  The final decision on winning projects will be made in early October.
 	</li>
	<li><strong>Are projects outside the Lower Mainland eligible?</strong><br />
            Yes, providing the developer is a member of UDI.  For example, a member of UDI Okanagan is eligible to submit a project in any of the categories.  In addition, there will be two special Awards for the Best of the Okanagan and the Best of Vancouver Island.
 	</li>
	<li><strong>Is it possible to have duplicates of the Awards made recognizing members of the project team?</strong><br />
            Yes, after the Awards ceremony, extra Awards recognizing the team will be made available for a fee.
 	</li>
	<li><strong>When is the ceremony and can I bring my spouse/guest?</strong><br />
            The ceremony will be held Friday, October 26th, 2012, at the Hyatt Regency Hotel.  This is a black tie optional event and spouses and guests are most welcome.
            <p>This is also an excellent event to invite your project team and local elected officials.</p>
	</li>
